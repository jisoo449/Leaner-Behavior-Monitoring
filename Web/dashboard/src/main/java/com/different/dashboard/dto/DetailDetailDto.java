package com.different.dashboard.dto;

public class DetailDetailDto {
	int id;
	int emotion;
	int blink;
	int gaze;
	int slope;
	int hand;
	int score;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getEmotion() {
		return emotion;
	}
	public void setEmotion(int emotion) {
		this.emotion = emotion;
	}
	public int getBlink() {
		return blink;
	}
	public void setBlink(int blink) {
		this.blink = blink;
	}
	public int getGaze() {
		return gaze;
	}
	public void setGaze(int gaze) {
		this.gaze = gaze;
	}
	public int getSlope() {
		return slope;
	}
	public void setSlope(int slope) {
		this.slope = slope;
	}
	public int getHand() {
		return hand;
	}
	public void setHand(int hand) {
		this.hand = hand;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
}
